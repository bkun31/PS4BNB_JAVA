    private void jButton_verifMerkelActionPerformed(java.awt.event.ActionEvent evt) {          
        if (jTextField_numMerkel.getText().equals("")){
            System.out.println("Case vide");
        }
        else if (chain  == null || chain.isEmpty()){
            System.out.println("BlockChain vide");
        }
        else {
            int numblock = Integer.parseInt(jTextField_numMerkel.getText()); 
            if (chain.getChaine().get(numblock).verifMerkel()){
                System.out.println("Merkel block n� " + numblock + " : OK");
            }
            else {
                System.out.println("Merkel block n� " + numblock + " : Incorrecte");
            }
        }
    }                                                   

