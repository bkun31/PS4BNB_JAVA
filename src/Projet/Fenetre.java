    private void jButton_startActionPerformed(java.awt.event.ActionEvent evt) {   
        int nbUser = Integer.parseInt(jTextField_nbUser.getText());
        int nbBlock = Integer.parseInt(jTextField_nbBlock.getText());
        int nbDiv = Integer.parseInt(jTextField_nbDiv.getText());
        int difficulte = Integer.parseInt(jTextField_difficulte.getText());
        int recompence = Integer.parseInt(jTextField_recompence.getText());

        chain = new BlockChain(nbUser);

        if (jRadioButton_print_true.isSelected()) chain.setPrint( true );
        if (jRadioButton_print_false.isSelected()) chain.setPrint( false );

        chain.setDifficulte(difficulte);
        chain.setNbBlockDiv(nbDiv);
        chain.setBlockMax(nbBlock);
        chain.setRecompenceBnb(recompence);
        
        chain.start();

    }                                             

